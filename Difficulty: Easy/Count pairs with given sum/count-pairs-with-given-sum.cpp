class Solution {
  public:
    int getPairsCount(const vector<int>& arr, int k) {
        unordered_map<int, int> freq;
        int cnt = 0;

        for (int num : arr) {
            int complement = k - num;
            if (freq.find(complement) != freq.end()) {
                cnt += freq[complement];
            }
            freq[num]++;
        }

        return cnt;

    }
};

